package com.bean;

public class BeanInteg {
	
	String name;
	String stuid;
	String place;
	String time;
	String detail;
	String type;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStuid() {
		return stuid;
	}
	public void setStuid(String stuid) {
		this.stuid = stuid;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	public String getData(int i)
	{
		switch (i)
		{
			case 0: return getName();
			case 1: return getStuid();
			case 2: return getTime();
			case 3: return getPlace();
			case 4: return getType();		
			case 5: return getDetail();
		
		}
		return "";
	}
	
	public String gotDetailtype(String type)
	{
		switch(type)
		{
		case "1": return "考试作弊";
		case "2": return "抄袭作业";
		case "3": return "其他";
		}
		return "";
	}

}
